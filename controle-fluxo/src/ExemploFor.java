/*
 * Exemplo com controle de incremento
 */

public class ExemploFor {
    public static void main(String[] args) {
        for(int caneirinhos = 1; caneirinhos <= 20; caneirinhos ++){
            System.out.println("Contando carneirinhos: " + caneirinhos);
        }

        System.out.println("Ana dormiu.");
    }
}

